<template>
  <div>
    <template
      v-if="
        Object.keys(this.$route.query).indexOf('tagId') !== -1 &&
          Object.keys(this.$route.query).indexOf('sortId') !== -1
      "
    >
      <component
        :is="currentLayout.videoMore"
        :set-header-title="setHeaderTitle"
        :set-has-search-btn="setHasSearchBtn"
        :siteId="currentLayout.siteId"
      />
    </template>

    <template v-else>
      <component
        :is="currentLayout.videoLobby"
        :set-header-title="setHeaderTitle"
        :set-has-search-btn="setHasSearchBtn"
        :siteId="currentLayout.siteId"
      />
    </template>
  </div>
</template>

<script>
import { mapGetters } from "vuex";
import { setCookie } from "@/lib/cookie";

export default {
  components: {
    // 套用 yabo 版面
    yaboVideoLobby: () => import("../layout/yabo/videoLobby"),
    yaboVideoMore: () => import("../layout/yabo/videoMore"),
    gayVideoLobby: () => import("../layout/yabo/videoLobby"),
    gayVideoMore: () => import("../layout/yabo/videoMore"),
    lesVideoLobby: () => import("../layout/yabo/videoLobby"),
    lesVideoMore: () => import("../layout/yabo/videoMore"),
    // 套用 custom 版面 - 小豬
    smallPigVideoLobby: () => import("../layout/custom/videoLobby"),
    smallPigVideoMore: () => import("../layout/custom/videoMore")
  },
  props: {
    source: {
      type: String,
      default: "yabo"
    },
    setHeaderTitle: {
      type: Function,
      default: () => {}
    },
    setHasSearchBtn: {
      type: Function,
      default: () => {}
    }
  },
  data() {
    return {
      currentLayout: {}
    };
  },
  computed: {
    ...mapGetters({
      siteConfig: "getSiteConfig"
    })
  },
  created() {
    switch (this.source) {
      case "yabo":
        setCookie("s_id", this.siteConfig.PORN_CONFIG.ID["YB"]);
        this.currentLayout = {
          siteId: 1,
          videoLobby: "yaboVideoLobby",
          videoMore: "yaboVideoMore",
          setHeaderTitle: () => {
            this.setHeaderTitle(
              localStorage.getItem("iframe-third-url-title") || "鸭脖视频"
            );
          }
        };
        break;

      case "smallPig":
        setCookie("s_id", this.siteConfig.PORN_CONFIG.ID["SP"]);
        this.currentLayout = {
          siteId: 2,
          videoLobby: "smallPigVideoLobby",
          videoMore: "smallPigVideoMore",
          setHeaderTitle: () => {
            this.setHeaderTitle(
              localStorage.getItem("iframe-third-url-title") || "小猪视频"
            );
          }
        };
        break;

      case "gay":
        setCookie("s_id", this.siteConfig.PORN_CONFIG.ID["GAY"]);
        this.currentLayout = {
          siteId: 3,
          videoLobby: "gayVideoLobby",
          videoMore: "gayVideoMore",
          setHeaderTitle: () => {
            this.setHeaderTitle(
              localStorage.getItem("iframe-third-url-title") || "男男视频"
            );
          }
        };
        break;

      case "les":
        setCookie("s_id", this.siteConfig.PORN_CONFIG.ID["LES"]);
        this.currentLayout = {
          siteId: 4,
          videoLobby: "lesVideoLobby",
          videoMore: "lesVideoMore",
          setHeaderTitle: () => {
            this.setHeaderTitle(
              localStorage.getItem("iframe-third-url-title") || "女女视频"
            );
          }
        };
        break;
    }
  },
  watch: {
    "$route.query"() {
      if (this.$route.query.sortId && this.$route.query.tagId) {
        this.setHeaderTitle(this.$text("S_FULL_HD_MOVIE", "全部高清影片"));
        return;
      }

      this.currentLayout.setHeaderTitle();
    }
  }
};
</script>

