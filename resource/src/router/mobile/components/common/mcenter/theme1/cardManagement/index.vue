<template>
  <div :class="[$style['wrap']]">
    <div :class="$style['header']">
      <div :class="$style['btn-prev']" @click="backPre">
        <img
          :src="
            $getCdnPath(
              `/static/image/common/btn_back_${
                themeTPL === 'porn1'
                  ? 'grey'
                  : themeTPL === 'ey1'
                  ? 'white'
                  : themeTPL === 'sg1'
                  ? 'black'
                  : null
              }.png`
            )
          "
          alt="back"
        />
      </div>

      <div :class="[$style['content'], 'clearfix']">
        <div :class="$style['title']">{{ headerTitle }}</div>
      </div>

      <!-- 詳細頁-編輯按鈕 -->
      <div
        v-if="showDetailButton"
        :class="$style['header-icon']"
        @click="syncStatusList.editDetail = true"
      >
        <img
          :src="
            $getCdnPath(
              `/static/image/${siteConfig.MOBILE_WEB_TPL}/common/btn_more.png`
            )
          "
          alt="more"
        />
      </div>

      <!-- 歷史錢包按鈕 -->
      <template v-if="['ey1'].includes(themeTPL)">
        <div
          v-if="showHistoryButton"
          :class="$style['header-icon']"
          @click="goToHistory"
        >
          <img
            :src="
              $getCdnPath('/static/image/ey1/mcenter/bankCard/ic_history.png')
            "
            alt="history"
          />
        </div>
      </template>
    </div>

    <div v-if="isReceive && isShowTab" :class="$style['tab-wrap']">
      <div
        v-for="(item, index) in tabItem"
        :key="`tab-${item.key}`"
        :class="[
          $style['tab-item'],
          { [$style['is-current']]: currentTab === index }
        ]"
        @click="setCurrentTab(index)"
      >
        {{ item.text }}
      </div>
      <div
        :class="$style['active-slider']"
        :style="{ left: `calc(25% + 50% * ${currentTab})` }"
      />
    </div>

    <component
      :is="currentPage"
      :is-show-tab="isShowTab"
      :set-page-status="setPageStatus"
      :status-list.sync="statusList"
      :add-bank-card-step.sync="addBankCardStep"
      :user-level-obj="userLevelObj"
    />
  </div>
</template>

<script>
import { mapGetters, mapActions } from "vuex";
import entryMixin from "@/mixins/mcenter/bankCard/index";

export default {
  components: {
    bankCardInfo: () =>
      import(
        /* webpackChunkName: 'bankCardInfo' */ "./components/bank/cardInfo"
      ),
    addBankCard: () =>
      import(/* webpackChunkName: 'addBankCard' */ "./components/bank/addCard"),
    walletCardInfo: () =>
      import(
        /* webpackChunkName: 'walletCardInfo' */ "./components/wallet/cardInfo"
      ),
    addWalletCard: () =>
      import(
        /* webpackChunkName: 'addWalletCard' */ "./components/wallet/addCard"
      )
  },
  mixins: [entryMixin],
  data() {
    return {};
  },
  computed: {
    ...mapGetters({
      memInfo: "getMemInfo",
      userLevelObj: "getUserLevels",
      siteConfig: "getSiteConfig"
    }),
    $style() {
      const style =
        this[`$style_${this.siteConfig.MOBILE_WEB_TPL}`] || this.$style_porn1;
      return style;
    },
    themeTPL() {
      return this.siteConfig.MOBILE_WEB_TPL;
    },
    isCommon() {
      return this.$route.meta.common;
    },
    tabItem() {
      return [
        {
          key: "bank",
          text: "银行卡"
        },
        {
          key: "wallet",
          text: ["porn1", "sg1"].includes(this.themeTPL)
            ? "数字货币"
            : "电子钱包"
        }
      ];
    },
    headerTitle() {
      const { type, redirect } = this.$route.query;
      const { hasRedirect, currentPage, themeTPL, isCommon } = this;
      const { showDetail } = this.statusList;

      // 非提現頁面跳轉過來 & 類型為銀行卡
      if (hasRedirect && type === "bankCard" && redirect !== "withdraw") {
        return "提现银行卡";
      }

      // 從其它頁面跳轉過來 & 類型為銀行卡
      if (hasRedirect && type === "bankCard") {
        return this.$text("S_ADD_BANKCARD", "添加银行卡");
      }

      // 從其它頁面跳轉過來 & 類型為錢包
      if (hasRedirect && type === "wallet") {
        switch (themeTPL) {
          case "porn1":
          case "sg1":
            return this.$text("S_ADD_DIGITAL_CURRENCY", "添加数字货币");

          case "ey1":
            return this.$text("S_ADD_VIRTUAL_BANKCARD", "添加电子钱包");
        }
      }

      // 根據目前頁面
      switch (currentPage) {
        // 卡片管理-銀行卡
        case "bankCardInfo":
          return showDetail
            ? this.$text("S_BANKCARD", "银行卡")
            : !isCommon
            ? this.$text("S_HISTORY_ACCOUNT", "历史帐号")
            : this.$text("S_CARD_MANAGEMENT", "卡片管理");

        // 卡片管理-錢包
        case "walletCardInfo":
          switch (themeTPL) {
            case "porn1":
            case "sg1":
              return showDetail
                ? this.$text("S_DIGITAL_CURRENCY", "数字货币")
                : !isCommon
                ? this.$text("S_HISTORY_ACCOUNT", "历史帐号")
                : this.$text("S_CARD_MANAGEMENT", "卡片管理");

            case "ey1":
              return showDetail
                ? this.$text("S_VIRTUAL_BANKCARD", "电子钱包")
                : !isCommon
                ? this.$text("S_HISTORY_ACCOUNT", "历史帐号")
                : this.$text("S_CARD_MANAGEMENT", "卡片管理");
          }

          break;

        // 添加銀行卡
        case "addBankCard":
          return this.$text("S_ADD_BANKCARD", "添加银行卡");

        // 添加錢包
        case "addWalletCard":
          switch (this.themeTPL) {
            case "porn1":
            case "sg1":
              return this.$text("S_ADD_DIGITAL_CURRENCY", "添加数字货币");

            case "ey1":
              return this.$text("S_ADD_VIRTUAL_BANKCARD", "添加电子钱包");
          }
          break;
      }
    },
    isOneTab() {
      // 常用錢包
      if (this.isCommon) {
        return !this.userLevelObj.bank || !this.userLevelObj.virtual_bank;
      }

      // 歷史錢包
      if (!this.isCommon) {
        // return (
        //   this.userLevelObj.bank_single ||
        //   this.userLevelObj.virtual_bank_single
        // );
        return this.userLevelObj.virtual_bank_single;
      }
    },
    showDetailButton() {
      const { memInfo, themeTPL, currentPage, userLevelObj } = this;
      const { showDetail, isAudit, hasSameTypeWallet } = this.statusList;

      const showButton =
        showDetail && !isAudit && memInfo.config.delete_bank_card;

      switch (currentPage) {
        case "bankCardInfo":
          return showButton;

        case "walletCardInfo":
          // 億元因還有「停用」與「移至歷史錢包」的選項，故不在此先判斷 delete_bank_card
          // 0419-億元 唯一不會出現的情況，單組開關 + 只有一張時
          return ["ey1"].includes(themeTPL)
            ? showDetail &&
                !isAudit &&
                (memInfo.config.delete_bank_card ||
                  !userLevelObj.virtual_bank_single ||
                  hasSameTypeWallet)
            : showButton;
      }
    },
    showHistoryButton() {
      const { isCommon, userLevelObj, currentPage } = this;
      const { showDetail } = this.statusList;

      // const bankHistory =
      //   ["bankCardInfo"].includes(currentPage) && !userLevelObj.bank_single;

      const walletHistory =
        ["walletCardInfo"].includes(currentPage) &&
        !userLevelObj.virtual_bank_single;

      const showButton = isCommon && !showDetail && walletHistory;

      return showButton;
    }
  },
  created() {
    if (!document.querySelector('script[data-name="esabgnixob"]')) {
      this.script = document.createElement("script");
      this.script.setAttribute("type", "text/javascript");
      this.script.setAttribute("data-name", "esabgnixob");
      this.script.setAttribute("src", "esabgnixob.js");

      document.head.appendChild(this.script);
    }

    this.actionSetUserdata(true);

    // 以下處理頁面設定
    this.actionSetUserLevels().then(() => {
      let type = this.$route.query.type;
      let tempType = localStorage.getItem("bankCardType");

      // 如果是從其它頁導轉過來，會進到添加卡片頁面，不用判斷開關(已 Set 為 False)
      if (this.hasRedirect || tempType) {
        if ((type && type === "bankCard") || tempType === "bankCard") {
          this.setPageStatus(0, "addBankCard", false);
        }

        if ((type && type === "wallet") || tempType === "wallet") {
          this.setPageStatus(1, "addWalletCard", false);
        }

        localStorage.removeItem("bankCardType");
        return;
      }

      // 銀行卡/電子錢包，其中有一方關閉(在常用錢包頁面)
      if (this.isCommon && this.isOneTab) {
        if (this.userLevelObj.bank) {
          this.setPageStatus(0, "bankCardInfo", false);
          return;
        }

        if (this.userLevelObj.virtual_bank) {
          this.setPageStatus(1, "walletCardInfo", false);
          return;
        }
      }

      // 銀行卡/電子錢包，其中有一方開啟多組開關(在歷史錢包頁面)
      if (!this.isCommon && this.isOneTab) {
        // if (!this.userLevelObj.bank_single) {
        //   this.setPageStatus(0, "bankCardInfo", false);
        //   return;
        // }

        if (!this.userLevelObj.virtual_bank_single) {
          this.setPageStatus(1, "walletCardInfo", false);
          return;
        }
      }

      // 預設頁面(預設為銀行卡頁面)
      // 常用：銀行卡 ＆ 歷史：電子錢包（因無歷史銀行卡）
      if (this.isCommon) this.setPageStatus(0, "bankCardInfo", true);
      if (!this.isCommon) this.setPageStatus(1, "walletCardInfo", false);
    });
  },
  methods: {
    ...mapActions(["actionSetUserdata", "actionSetUserLevels"]),
    setPageStatus(tabIndex, pageName, isShowTab) {
      this.isReceive = false;
      this.currentTab = tabIndex;
      this.currentPage = pageName;

      // 當 Tab 在某些頁面不用顯示，this.isShowTab = false
      if (!isShowTab) {
        this.isShowTab = false;
      } else {
        this.isShowTab = this.isOneTab ? false : true;
      }

      this.isReceive = true;
    },
    setCurrentTab(index) {
      this.currentTab = index;
      switch (index) {
        case 0:
          this.currentPage = "bankCardInfo";
          break;

        case 1:
          this.currentPage = "walletCardInfo";
          break;
      }
    },
    backPre() {
      //刪除數字貨幣路徑 刪除選擇卡片
      localStorage.removeItem("oneClickBindingMode");
      localStorage.removeItem("isBackFromService");
      localStorage.removeItem("selectTarget");
      // 目前只有銀行卡有分兩階段
      if (
        this.currentPage === "addBankCard" &&
        this.addBankCardStep === "two"
      ) {
        this.step = "one";
        return;
      }

      // 從其它頁面進入到此頁面(通常停留在添加卡片的頁面)
      let redirect = this.$route.query.redirect;
      if (redirect) {
        switch (redirect) {
          case "home":
            this.$router.push("/mobile");
            break;

          case "liveStream":
            this.$router.push("/mobile/liveStream");
            break;

          default:
            this.$router.back();
            // this.$router.push(`/mobile/mcenter/${redirect}`);
            break;
        }
        return;
      }

      switch (this.currentPage) {
        // 當頁面停留在卡片管理
        case "bankCardInfo":
        case "walletCardInfo":
          // 卡片管理-詳細頁面
          if (this.statusList.showDetail) {
            this.statusList.showDetail = false;

            this.setPageStatus(
              this.currentTab,
              this.currentPage,
              this.$route.name != "mcenter-historyCard"
            );
            return;
          }

          this.$router.back();
          return;

        // 當頁面停留在添加卡片
        case "addBankCard":
          this.setPageStatus(0, "bankCardInfo", true);
          return;

        case "addWalletCard":
          this.setPageStatus(1, "walletCardInfo", true);
          return;
      }
      this.$router.back();
    }
  }
};
</script>

